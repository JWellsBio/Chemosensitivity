# neural net
data(longley)
x <- longley[, 1:6]
y <- longley[, 7]
fit <- nnet(Employed ~ ., longley, size = 12, maxit = 500, linout = T, decay = 0.01)
summary(fit)
predictions <- predict(fit, x, type = 'raw')
mse <- mean((y - predictions) ^ 2)
