## MARS
#data
data(longley)
#fit model
fit <- earth(Employed ~ ., longley)
#summarize
summary(fit)
# summarize importance of input vars
evimp(fit)
# make predictions
predictions <- predict(fit, longley)
# summarize accuracy
mse <- mean((longley$Employed - predictions) ^ 2)
mse #0.0457



## SVM
data(longley)
